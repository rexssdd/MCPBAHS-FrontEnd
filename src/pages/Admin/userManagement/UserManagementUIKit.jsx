export const DataTable = ({ columns, rows, selected, onToggleAll, onToggleOne, renderActions }) => {
  const allChecked  = rows.length > 0 && rows.every(r => selected.includes(r.id));
  const someChecked = selected.length > 0 && !allChecked;
  return (
    <div className="data-table-wrapper">
      <table className="data-table">
        <thead>
          <tr className="data-table__head-row">
            <th className="data-table__th data-table__th--check">
              <Checkbox checked={allChecked} indeterminate={someChecked} onChange={onToggleAll}/>
            </th>
            {columns.map(c => <th key={c.key} className="data-table__th">{c.label}</th>)}
            <th className="data-table__th data-table__th--actions">Actions</th>
          </tr>
        </thead>
        <tbody>
          {rows.length === 0 && (
            <tr><td colSpan={columns.length + 2} className="data-table__empty">No users found.</td></tr>
          )}
          {rows.map(row => (
            <tr key={row.id} className="data-table__row">
              <td className="data-table__td data-table__td--check">
                <Checkbox checked={selected.includes(row.id)} onChange={()=>onToggleOne(row.id)}/>
              </td>
              {columns.map(c => (
                <td key={c.key} className={[
                  "data-table__td",
                  c.bold  ? "data-table__td--bold"  : "",
                  c.link  ? "data-table__td--link"  : "",
                  c.muted ? "data-table__td--muted" : "",
                ].join(" ")}>
                  {c.render ? c.render(row) : row[c.key]}
                </td>
              ))}
              <td className="data-table__td data-table__td--actions">
                <div className="data-table__actions">{renderActions(row)}</div>
              </td>
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
};

