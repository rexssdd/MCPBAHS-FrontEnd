export const Pagination = ({ page, total, perPage, onChange }) => {
  const tp = Math.max(1, Math.ceil(total / perPage));
  if (tp <= 1) return null;
  const pages = [];
  if (tp <= 7) { for (let i=1;i<=tp;i++) pages.push(i); }
  else { pages.push(1); if(page>3)pages.push("…"); for(let i=Math.max(2,page-1);i<=Math.min(tp-1,page+1);i++)pages.push(i); if(page<tp-2)pages.push("…"); pages.push(tp); }
  return (
    <div className="pagination">
      <button disabled={page===1} onClick={()=>onChange(page-1)} className="pagination__btn pagination__btn--inactive">
        <ChevLeftIcon/> Previous
      </button>
      {pages.map((p,i)=>p==="…"
        ? <span key={"e"+i} className="pagination__ellipsis">…</span>
        : <button key={p} onClick={()=>onChange(p)} className={`pagination__btn ${page===p?"pagination__btn--active":"pagination__btn--inactive"}`}>{p}</button>
      )}
      <button disabled={page===tp} onClick={()=>onChange(page+1)} className="pagination__btn pagination__btn--inactive">
        Next <ChevRightIcon/>
      </button>
    </div>
  );
};

